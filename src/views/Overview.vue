  const typeParam = router.currentRoute.value.query.type as string | undefined
  if (typeParam && typeParam !== newType) {
    router.replace({
      path: '/overview',
      query: {},
    })
  }

  // Âä†ËΩΩÊñ∞Á±ªÂûãÁöÑËèúÂçïÊï∞ÊçÆÔºàÂª∂Ëøü‰∏ÄÁÇπÁÇπÊâßË°åÔºåÈÅøÂÖçÂø´ÈÄüÂàáÊç¢Ëß¶ÂèëÂ§öÊ¨°ËØ∑Ê±ÇÔºâ
  setTimeout(() => {
