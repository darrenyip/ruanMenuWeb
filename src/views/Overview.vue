// Âä†ËΩΩËèúÂçïÊï∞ÊçÆ
const loadMenuData = async (type: MenuType) => {
  try {
    // Ëé∑Âèñ‰ªäÂ§©ÁöÑÊó•ÊúüÔºåÊ†ºÂºè‰∏∫ YYYY-MM-DD
    const today = new Date().toISOString().split('T')[0]

    // ÊòæÁ§∫Âä†ËΩΩÁä∂ÊÄÅ
    menuStore.loading = true

    // ‰ΩøÁî®storeËé∑ÂèñËèúÂçïÊï∞ÊçÆ
