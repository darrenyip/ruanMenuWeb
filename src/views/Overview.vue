    console.log(`Â∑≤Âä†ËΩΩ${type}ËèúÂçïÊï∞ÊçÆ:`, menuStore.currentMenu)
  } catch (error) {
    console.error(`Âä†ËΩΩ${type}ËèúÂçïÂ§±Ë¥•:`, error)

    // Â¶ÇÊûúÊòØÂèñÊ∂àËØ∑Ê±ÇÁöÑÈîôËØØÔºå‰∏çÊòæÁ§∫ÈîôËØØ‰ø°ÊÅØ
    if (
      error instanceof Error &&
      (error.message.includes('cancelled') || error.message.includes('autocancelled'))
    ) {
      console.log('ËØ∑Ê±ÇË¢´ÂèñÊ∂àÔºåÂèØËÉΩÊòØÁî±‰∫éÈáçÂ§çËØ∑Ê±ÇÂØºËá¥')
      return
    }

    // ÁÆÄÂåñÈîôËØØÊ∂àÊÅØ
    let errorMsg = 'Âä†ËΩΩËèúÂçïÂ§±Ë¥•'
    if (error instanceof Error) {
      // ÂØπÁâπÂÆöÈîôËØØËøõË°åÂèãÂ•ΩÂ§ÑÁêÜ
      if (error.message.includes('Ê≤°ÊúâÊâæÂà∞')) {
        errorMsg = `‰ªäÊó•ÊöÇÊó†${type === 'lunch' ? 'ÂçàÈ§ê' : type === 'dinner' ? 'ÊôöÈ§ê' : 'ÂÖ∂‰ªñ'}ËèúÂçï`
      } else {
        errorMsg = error.message
      }
    }
    ElMessage.error(errorMsg)
  } finally {
    // Á°Æ‰øùloadingÁä∂ÊÄÅË¢´ÈáçÁΩÆ
    menuStore.loading = false
  }
}

// ÁõëÂê¨ËèúÂçïÁ±ªÂûãÂèòÂåñ
watch(currentMealType, (newType) => {
